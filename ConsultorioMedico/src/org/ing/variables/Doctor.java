package org.ing.variables;

/**
 *
 * @author Lupita
 */
public class Doctor {
    private int cveD;
    private String nombreD;
    private String apellidosD;
    private String direccionD;
    private String especialidad;
    private String curpD;
    private String telefonoD;

    public int getCveD() {
        return cveD;
    }

    public void setCveD(int cveD) {
        this.cveD = cveD;
    }

    public String getNombreD() {
        return nombreD;
    }

    public void setNombreD(String nombreD) {
        this.nombreD = nombreD;
    }

    public String getApellidosD() {
        return apellidosD;
    }

    public void setApellidosD(String apellidosD) {
        this.apellidosD = apellidosD;
    }

    public String getDireccionD() {
        return direccionD;
    }

    public void setDireccionD(String direccionD) {
        this.direccionD = direccionD;
    }

    public String getEspecialidad() {
        return especialidad;
    }

    public void setEspecialidad(String especialidad) {
        this.especialidad = especialidad;
    }

    public String getCurpD() {
        return curpD;
    }

    public void setCurpD(String curpD) {
        this.curpD = curpD;
    }

    public String getTelefonoD() {
        return telefonoD;
    }

    public void setTelefonoD(String telefonoD) {
        this.telefonoD = telefonoD;
    }
    
    
    
}
